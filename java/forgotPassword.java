    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        int check=0;
        String securityQuestion=jTextField2.getText();
        String answer=jTextField3.getText();
        String newPassword=jPasswordField1.getText();
        if(answer.equals("")|| newPassword.equals(""))
        {
            check=1;
            JOptionPane.showConfirmDialog(null,"All Field is Required");
        }
        else
        {
            ResultSet rs=Select.getData("select *from user where email='"+email+"' and securityQuestion='"+securityQuestion+"' and answer='"+answer+"'");
            try
            {
                if(rs.next())
                {
                check=1;
                InsertUpdateDelete.setData("update user set password='"+newPassword+"' where email='"+email+"'", "Password Set Successfully");
                setVisible(false);
                new forgotPassword().setVisible(true);
                }
            }
            catch(Exception e)
            {
                JOptionPane.showConfirmDialog(null, e);
            }
        }
        if(check==0)
            JOptionPane.showConfirmDialog(null,"Incorrect Answer");
    }//GEN-LAST:event_jButton3ActionPerformed

